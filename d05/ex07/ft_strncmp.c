int		ft_strncmp(char *s1, char *s2, unsigned int nb)
{
	unsigned int i;

	i = 0;
	while (i < nb)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		if (s1[i] == '\0' && s2[i] == '\0')
			return (0);
		i++;
	}
	return (0);
}
